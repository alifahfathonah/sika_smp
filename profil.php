                   <p class="justify"> Surat: Muhammad, Ayat 7:
“Hai orang-orang yang beriman, jika kamu menolong (agama) Allah, niscaya Dia akan menolongmu dan meneguhkan kedudukanmu.”
Berangkat dari ayat tersebut bahwa niat kami mendirikan Pondok Pesantren Modern Nur Barokah Al – Wadani dengan jenjang Sekolah Menengah Pertama Islam Terpadu (SMP IT) merupakan bagian dari ikhtiar kami untuk mensyiarkan agama Islam. 
Pendidikan menjadi salah satu wadah yang kami bentuk karena pendidikan merupakan bagian dari kehidupan manusia yang tidak dapat dipisahkan. Modernisasi menjadi bagian dari kehidupan manusia yang tidak dapat dielakkan, kehidupan ini akan menjadi bagian yang menakutkan ataukah akan menjadi bagian yang menyenangkan dan memberikan manfaat positif untuk keberlangsungan dan kebermanfaat umat manusia.</p>

<p class="justify"> Dampak positif dan negatif tentunya akan hadir dan menjadi bagian tantangan tersendiri serta menjadi pilihan yang harus dipilih oleh manusia.
Pilihan dalam kehidupan tentunya harus mengambil pada hal-hal yang bernilai positif namun adakalanya manusia memilih pada hal-hal yang bernilai negatif. Maka untuk turut serta berkontribusi agar manusia tidak salah memilih jalan, kami mendirikan sekolah yang bermaksud untuk membuka cakrawala berpikir mereka untuk dunia yang lebih luas dan cerdas untuk memilih jalan hidup yang bermanfaat di dunia dan akhirat. </p>

<p class="justify">Pendidikan pada jenjang Sekolah Menengah Pertama (SMP) kami pilih karena pada masa-masa ini manusia membutuhkan bimbingan yang lebih ketat. Masa anak-anak yang menuju pada masa remaja sangat rentan terpengaruh pada hal-hal yang negatif, maka sudah sepatutnya perlindungan akan mereka lebih ditingkatkan. Dunia luar menjadi salah satu penentu dan kuat untuk merubah perilaku manusia, untuk menjadi baik atau buruk. Proteksi untuk tidak masuknya pikiran-pikiran negatif dapat dimulai dari pendidikan ini, karena pendidikan yang ada di SMPIT Nur Barokah Al – Wadani yang merupakan bagian dari Yayasan Nur Barokah Al- Wadani tidak hanya menekankan pada kecerdasan intelektual saja, tetapi kami juga membimbing mereka untuk memiliki kecerdasan emosional dan kecerdasan spiritual.</p>



